import axios from "axios";

export const getDocument = async (id) => {
  return axios
    .get(`/document${id}`)
    .then((response) => {
      if (response.data.success) {
        return {
          success: response.data.success,
          data: response.data.data,
        };
      }
    })
    .catch((error) => {
      return {
        error: error.message,
      };
    });
};
